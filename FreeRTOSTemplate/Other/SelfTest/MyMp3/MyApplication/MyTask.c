/* Includes ------------------------------------------------------------------*/
#include "MyApplication.h"

/* Private define-------------------------------------------------------------*/

/* 句柄Private variables----------------------------------------------------------*/
static TaskHandle_t xLED0Task_Handle = NULL;
static TaskHandle_t xLED1Task_Handle = NULL;

/* 声明Private function prototypes------------------------------------------------*/
static void AppTaskCreate(void);

static void vLED0_Task(void *pvParameters);
static void vLED1_Task(void *pvParameters);

/* Public variables-----------------------------------------------------------*/
MyTask_t MyTask = 
{
	AppTaskCreate
};      

/*
	* @name   AppTaskCreate
	* @brief  创建应用任务
	* @param  None
	* @retval None      
*/
static void AppTaskCreate()
{
	//LED0测试任务
	xTaskCreate(vLED0_Task,  //任务函数
  	"vLED0_Task",            //任务名称
  	512,                     //任务栈大小，单位word(4字节)
	NULL,                    //任务参数
	1,                       //任务优先级
	&xLED0Task_Handle);      //任务句柄
	
	//LED1测试任务
	xTaskCreate(vLED1_Task,  //任务函数
  	"vLED1_Task",            //任务名称
	512,                    //任务栈大小，单位word(4字节)
	NULL,                   //任务参数
	2,                      //任务优先级
	&xLED1Task_Handle);      //任务句柄
	
}

/*
	* @name   vLED0_Task
	* @brief  LED1任务
	* @param  None
	* @retval None      
*/
static void vLED0_Task(void *pvParameters)
{
	 while(1)
	 {		 
		Delay.delay_us(500000);
		HAL_GPIO_TogglePin(LED0_GPIO_Port,LED0_Pin); 
		vTaskDelay(1000);
	 } 
}

/*
	* @name   vLED1_Task
	* @brief  LED1任务
	* @param  None
	* @retval None      
*/
static void vLED1_Task(void *pvParameters)
{
	 while(1)
	 {		 
		Delay.delay_us(1000000);
		HAL_GPIO_TogglePin(LED1_GPIO_Port,LED1_Pin); 
		vTaskDelay(500);
	 } 
}


